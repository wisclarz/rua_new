  Future<void> changePassword({
    required String currentPassword,
    required String newPassword,
  }) async {
    try {
      final user = _auth.currentUser;
      if (user == null) throw Exception('Kullanƒ±cƒ± oturumu bulunamadƒ±');

      final credential = firebase_auth.EmailAuthProvider.credential(
        email: user.email!,
        password: currentPassword,
      );
      
      await user.reauthenticateWithCredential(credential);
      await user.updatePassword(newPassword);
      
    } on firebase_auth.FirebaseAuthException catch (e) {
      throw Exception(_handleAuthException(e));
    } catch (e) {
      throw Exception('≈ûifre deƒüi≈ütirilirken hata olu≈ütu: $e');
    }
  }

  /// Hesabƒ± tamamen sil - T√ºm giri≈ü y√∂ntemleri i√ßin √ßalƒ±≈üƒ±r
  /// Google, telefon numarasƒ± veya email/password
  Future<void> deleteAccount(String password) async {
    try {
      final user = _auth.currentUser;
      if (user == null) throw Exception('Kullanƒ±cƒ± oturumu bulunamadƒ±');

      debugPrint('üóëÔ∏è Starting account deletion for user: ${user.uid}');

      // Eƒüer password ile giri≈ü yapƒ±lmƒ±≈üsa re-authenticate et
      if (password.isNotEmpty && user.email != null && user.email!.isNotEmpty) {
        debugPrint('üîê Re-authenticating with email/password...');
        final credential = firebase_auth.EmailAuthProvider.credential(
          email: user.email!,
          password: password,
        );
        await user.reauthenticateWithCredential(credential);
      }
      // Google ile giri≈ü yapƒ±lmƒ±≈üsa, Google ile re-authenticate et
      else if (user.providerData.any((p) => p.providerId == 'google.com')) {
        debugPrint('üîê Re-authenticating with Google...');
        try {
          final GoogleSignInAccount? googleUser = await _googleSignIn.signIn();
          if (googleUser == null) {
            throw Exception('Google ile tekrar giri≈ü yapƒ±lmasƒ± gerekiyor');
          }

          final GoogleSignInAuthentication googleAuth = await googleUser.authentication;
          final credential = firebase_auth.GoogleAuthProvider.credential(
            accessToken: googleAuth.accessToken,
            idToken: googleAuth.idToken,
          );

          await user.reauthenticateWithCredential(credential);
        } catch (e) {
          throw Exception('Google ile tekrar kimlik doƒürulama ba≈üarƒ±sƒ±z: $e');
        }
      }
      // Telefon numarasƒ± ile giri≈ü yapƒ±lmƒ±≈üsa, re-authenticate gerekmiyor
      else if (user.providerData.any((p) => p.providerId == 'phone')) {
        debugPrint('üì± Phone auth detected - no re-authentication needed');
      }

      // 1. FCM token'ƒ± temizle (√∂nce temizle ki bildirim gitmesin)
      try {
        debugPrint('üóëÔ∏è Clearing FCM token before account deletion...');
        await _clearFCMTokenOnSignOut();
      } catch (e) {
        debugPrint('‚ö†Ô∏è FCM token clear error (non-critical): $e');
      }

      // 2. Kullanƒ±cƒ± verilerini sil
      debugPrint('üóëÔ∏è Deleting user data from Firestore...');
      await _deleteUserData(user.uid);

      // 3. Google Sign-In cache'ini temizle
      try {
        final isSignedIn = await _googleSignIn.isSignedIn();
        if (isSignedIn) {
          await _googleSignIn.signOut();
          debugPrint('‚úÖ Google Sign-In cache cleared');
        }
      } catch (e) {
        debugPrint('‚ö†Ô∏è Google sign-out error (non-critical): $e');
      }

      // 4. Son olarak Firebase Auth hesabƒ±nƒ± sil
      debugPrint('üóëÔ∏è Deleting Firebase Auth account...');
      await user.delete();

      debugPrint('‚úÖ Account deletion completed successfully');

    } on firebase_auth.FirebaseAuthException catch (e) {
      debugPrint('‚ùå Firebase Auth exception during account deletion: ${e.code}');
      throw Exception(_handleAuthException(e));
    } catch (e) {
      debugPrint('‚ùå Error during account deletion: $e');
      throw Exception('Hesap silinirken hata olu≈ütu: $e');
    }
  }

