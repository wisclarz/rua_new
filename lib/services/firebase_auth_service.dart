  Future<void> signOut() async {
    try {
      debugPrint('üö™ Starting sign out...');

      // 1. Clear FCM token (√∂nce temizle ki bildirim gitmesin)
      try {
        debugPrint('üóëÔ∏è Clearing FCM token before sign out...');
        await _clearFCMTokenOnSignOut();
      } catch (e) {
        debugPrint('‚ö†Ô∏è FCM token clear error (non-critical): $e');
      }

      // 2. Sign out from Google (clear cache)
      try {
        final isSignedIn = await _googleSignIn.isSignedIn();
        if (isSignedIn) {
          await _googleSignIn.signOut();
          debugPrint('‚úÖ Google sign out complete');
        }
      } catch (e) {
        debugPrint('‚ö†Ô∏è Google sign out error (non-critical): $e');
      }

      // 3. Sign out from Firebase
      await _auth.signOut();
      debugPrint('‚úÖ Firebase sign out complete');

    } catch (e) {
      debugPrint('‚ùå Sign out error: $e');
      throw Exception('√áƒ±kƒ±≈ü yapƒ±lƒ±rken hata olu≈ütu: $e');
    }
  }

  /// FCM token'ƒ± temizle (sign out i√ßin)
  Future<void> _clearFCMTokenOnSignOut() async {
    try {
      final user = _auth.currentUser;
      if (user == null) return;

      debugPrint('üóëÔ∏è Clearing FCM tokens and marking user as logged out: ${user.uid}');

      // 1. Users koleksiyonundan FCM token'ƒ± temizle VE isLoggedIn: false yap
      await _firestore.collection('users').doc(user.uid).update({
        'fcmToken': FieldValue.delete(),
        'fcmTokenUpdatedAt': Timestamp.fromDate(DateTime.now()),
        'isLoggedIn': false, // üîê Mark user as logged out
      });
      debugPrint('‚úÖ FCM token cleared and user marked as logged out');

      // 2. ‚ö†Ô∏è √ñNEMLƒ∞: Dreams koleksiyonundaki T√úM fcmToken'larƒ± temizle
      // n8n workflow'u dreams koleksiyonundaki fcmToken'ƒ± kullanarak bildirim g√∂nderiyor
      final dreamsQuery = await _firestore
          .collection('dreams')
          .where('userId', isEqualTo: user.uid)
          .get();

      if (dreamsQuery.docs.isNotEmpty) {
        final batch = _firestore.batch();
        int updateCount = 0;

        for (final doc in dreamsQuery.docs) {
          // Sadece fcmToken field'ƒ± varsa g√ºncelle
          if (doc.data().containsKey('fcmToken')) {
            batch.update(doc.reference, {
              'fcmToken': FieldValue.delete(),
            });
            updateCount++;
          }
        }

        if (updateCount > 0) {
          await batch.commit();
          debugPrint('‚úÖ FCM tokens cleared from $updateCount dreams');
        } else {
          debugPrint('‚ÑπÔ∏è No dreams with fcmToken found');
        }
      } else {
        debugPrint('‚ÑπÔ∏è No dreams found for user');
      }

      debugPrint('‚úÖ All FCM tokens cleared successfully for user: ${user.uid}');
    } catch (e) {
      debugPrint('‚ö†Ô∏è Failed to clear FCM tokens on sign out: $e');
      // Non-critical error, continue sign out
    }
  }

