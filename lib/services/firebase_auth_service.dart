  Future<void> _createUserProfile(app_models.User user) async {
    // ‚ö†Ô∏è Yeni kullanƒ±cƒ± olu≈üturulurken MUTLAKA isLoggedIn: true olmalƒ±
    final userData = user.toJson();
    userData['isLoggedIn'] = true; // üîê Force set to true for new users

    await _firestore.collection('users').doc(user.id).set(userData);
    debugPrint('‚úÖ New user profile created with isLoggedIn: true');
  }

  /// üîê Check if user is currently logged in (optimized - only fetches isLoggedIn field)
  /// Bu metod sadece isLoggedIn field'ƒ±nƒ± okur, t√ºm user verisini √ßekmez
  Future<bool> isUserLoggedIn(String userId) async {
    try {
      final doc = await _firestore
          .collection('users')
          .doc(userId)
          .get();

      if (!doc.exists) {
        debugPrint('‚ö†Ô∏è User document not found: $userId');
        return false;
      }

      final isLoggedIn = doc.data()?['isLoggedIn'] ?? false;
      debugPrint('üîê User $userId isLoggedIn: $isLoggedIn');
      return isLoggedIn;
    } catch (e) {
      debugPrint('‚ùå Error checking isLoggedIn: $e');
      return false;
    }
  }

