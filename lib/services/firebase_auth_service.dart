  Future<void> _deleteUserData(String userId) async {
    try {
      debugPrint('üóëÔ∏è Deleting all user data for: $userId');

      // ‚ö†Ô∏è √ñNEMLƒ∞: Kullanƒ±cƒ± dok√ºmanƒ±nƒ± EN SON siliyoruz
      // √á√ºnk√º kullanƒ±cƒ± dok√ºmanƒ± silindiƒüinde, diƒüer koleksiyonlara eri≈üim izni kayboluyor

      // 1. R√ºyalarƒ± sil
      final dreamsQuery = await _firestore
          .collection('dreams')
          .where('userId', isEqualTo: userId)
          .get();

      if (dreamsQuery.docs.isNotEmpty) {
        final dreamsBatch = _firestore.batch();
        for (final doc in dreamsQuery.docs) {
          dreamsBatch.delete(doc.reference);
        }
        await dreamsBatch.commit();
        debugPrint('‚úÖ ${dreamsQuery.docs.length} dreams deleted');
      }

      // 2. R√ºya analizlerini sil
      try {
        final analysesQuery = await _firestore
            .collection('dream_analyses')
            .where('userId', isEqualTo: userId)
            .get();

        if (analysesQuery.docs.isNotEmpty) {
          final analysesBatch = _firestore.batch();
          for (final doc in analysesQuery.docs) {
            analysesBatch.delete(doc.reference);
          }
          await analysesBatch.commit();
          debugPrint('‚úÖ ${analysesQuery.docs.length} dream analyses deleted');
        } else {
          debugPrint('‚ÑπÔ∏è No dream analyses to delete');
        }
      } catch (e) {
        debugPrint('‚ö†Ô∏è Could not delete dream analyses (permission or not found): $e');
        // Continue anyway - this is non-critical
      }

      // 3. Abonelik bilgisini sil
      try {
        await _firestore.collection('subscriptions').doc(userId).delete();
        debugPrint('‚úÖ Subscription deleted');
      } catch (e) {
        debugPrint('‚ÑπÔ∏è Subscription not found: $e');
      }

      // 4. ƒ∞statistikleri sil (varsa)
      try {
        await _firestore.collection('user_stats').doc(userId).delete();
        debugPrint('‚úÖ User stats deleted');
      } catch (e) {
        debugPrint('‚ÑπÔ∏è User stats not found (already deleted or never created)');
      }

      // 5. Kullanƒ±cƒ± tercihlerini sil (varsa)
      try {
        await _firestore.collection('user_preferences').doc(userId).delete();
        debugPrint('‚úÖ User preferences deleted');
      } catch (e) {
        debugPrint('‚ÑπÔ∏è User preferences not found (already deleted or never created)');
      }

      // 6. EN SON: Kullanƒ±cƒ± dok√ºmanƒ±nƒ± sil
      await _firestore.collection('users').doc(userId).delete();
